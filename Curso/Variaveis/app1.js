//variáveis são maneiras de armazenar informações

//var maneira antiga = permite que adicione informação e depois alterar
//let = adicionar um valor nele e depois podemos alternar
//const = adicionamos um valor e não podemos substituir esse valor

var nome = "Fabio";
var nome = "Nicole";
console.log(`meu nome é ${nome}`);

//LET = ADICIONAR UM VALOR NELE E TAMBEM PODEMOS ALTERAR ESSE VALOR

let nomeLet = "Favio";

nomeLet = "Goku";
console.log(`meu nome é ${nomeLet}`);

//CONST = ADICIONAMOS UM VALOR E NÃO PODEMOS MAIS SUBSTITUIR ESSE VALOR
const nomeConst = "Daniel";

console.log(`meu nome é: ${nomeConst}`);
