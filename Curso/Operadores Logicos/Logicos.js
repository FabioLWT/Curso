// operadores logicos

// and logico (&&)
// para que retorne um verdadeiro (true) as duas condições devem ser verdadeiras
// true && true = true
// false && false = true
// true && false = false
// false && true = false
console.log("&&");
console.log("true && true ", true && true);
console.log("false && true ", false && true);
console.log("10 >=10 && 11 >= 11; ", 10 >= 10 && 11 >= 11);
console.log("===================================================")
// or logico (||)

console.log("||");
console.log("true || true ", true || true);
console.log("false || true ", false || true);
console.log("10 >=10 || 11 >= 11; ", 10 >= 10 || 11 >= 11);
console.log("===================================================")

// not lógico (!)
console.log("!");

